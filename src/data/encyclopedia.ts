// --- Helper Functions ---

export function getEntriesBySection(sectionId: EncyclopediaSectionId): EncyclopediaEntry[] {
  return encyclopediaEntries.filter(e => e.section === sectionId)
}

export function getEntryBySlug(slug: string): EncyclopediaEntry | undefined {
  return encyclopediaEntries.find(e => e.slug === slug)
}

export function searchEncyclopedia(query: string): EncyclopediaEntry[] {
  const q = query.toLowerCase()
  return encyclopediaEntries.filter(e =>
    e.term.toLowerCase().includes(q) ||
    e.shortDefinition.toLowerCase().includes(q) ||
    e.tags.some(t => t.toLowerCase().includes(q))
  )
}
